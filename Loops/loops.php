<?php

// For Loop

for($i = 1; $i < 10; $i ++){
    echo $i . " Hello World <br>";
}

for($i = 10; $i >= 1; $i --){
    echo $i . " Hello World <br>";
}

// While Loop

$a = 1;
while ($a <=10){
    echo "this is while loop <br>";
    $a ++;
}

// Do while

$b = 1;
do{
    echo "hello world <br>";
    $b ++;
}
while($b <=10);


?>
